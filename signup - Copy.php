var ea = {
    toggleForm: function () {
        if  ($jq('#updateEa:visible').length > 0) {
            this.hideForm();
        } else {
            this.showForm();
        }
        $jq('#updateEa').slideToggle();
    },
    showForm: function() {

        $jq( '.visitorActions input[name=_save]' ).attr( 'disabled', 'disabled' );
        $jq( '#updateEaSuccess' ).hide();
    },

    hideForm: function() {
        $jq( '.visitorActions input[name=_save]' ).removeAttr( 'disabled' );
        $jq( '#eaUpdateErrors' ).hide();
        $jq( '#updateEaForm :text' ).val( '' );
    },

    update: function() {

        $jq( '#eaUpdateErrors' ).hide();

        $jq.ajax( { type: 'POST',
                    url: $jq( '#updateEaForm' ).attr( 'action' ),
                    dataType: "json",
                    data: $jq( '#updateEaForm' ).serialize(),
                    success: function( data, textStatus ) { ea._handleUpdateSuccess( data ); },
                    error: function( request, textStatus, errorThrown ) { ea._handleUpdateError( errorThrown ); },
                    beforeSend: function( request ) { $jq( '#eaUpdateWait' ).toggle(); },
                    complete: function( request, textStatus ) { $jq( '#eaUpdateWait' ).toggle(); } } );
    },

    _handleUpdateSuccess: function( rsp ) {

        if ( rsp.status == 'failure' ) {

            this._displayError( rsp.fieldErrors.emailAddr );
            return;
        }

        $jq( '#emailAddr' ).text( rsp.ea.emailAddr );
        $jq( '#updateEaForm input[name=oldEmailAddr]' ).val( rsp.ea.emailAddr );
        $jq( '#updateEaSuccess' ).show();
        this.hideForm();
    },

    _handleUpdateError: function( error ) {

        this._displayError( 'We are unable to update your email address at this time' );
    },

    _displayError: function( error ) {

        error = error.replace( /\.ea\.[\w]+$/, '' ); // Strip off .ea.emailAddr
        $jq( '#eaUpdateErrors' ).text( error );
        $jq( '#eaUpdateErrors' ).fadeIn( 'slow' );
    }
};

